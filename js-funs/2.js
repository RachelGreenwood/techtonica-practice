// Write a function that takes a string as the argument.
// The function should return a number that is the number of characters in the string.
// Choose names that make the code easy to understand.

// Input: string
// Output: number (number of characters in the string)
// Return str length using length property

function strLength(str) {
    return str.length;
}

console.log(strLength("H"));
console.log(strLength("Hi"));
console.log(strLength("Hello World"));
console.log(strLength(""));